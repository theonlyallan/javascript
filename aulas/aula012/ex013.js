var agora = new Date()
var diaSem = agora.getDay()
// 0 = domingo
// 1 = segunda
// 2 = terça
// 3 = quarta 
// 4 = quinta 
// 5 = sexta
// 6 = sabado
switch (diaSem) {
    case 0:
        console.log('Domingo')
        break
}
switch (diaSem) {
    case 1:
        console.log('Segunda')
        break
}
switch (diaSem) {
    case 2:
        console.log('Terça')
        break
}
switch (diaSem) {
    case 3:
        console.log('Quarta')
        break
}
switch (diaSem) {
    case 4:
        console.log('Quinta')
        break
}
switch (diaSem) {
    case 5:
        console.log('Sexta')
        break
}
switch (diaSem) {
    case 6:
        console.log('Sabádo')
        break
}